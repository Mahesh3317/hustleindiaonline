export const getPostsByCategory = (category: string): BlogPost[] => {
  return mockPosts.filter(post => post.category === category);
};

export const getFeaturedPosts = (): BlogPost[] => {
  return mockPosts.filter(post => post.featured);
};

export const getLatestPosts = (limit: number = 10): BlogPost[] => {
  return mockPosts
    .sort((a, b) => new Date(b.publishDate).getTime() - new Date(a.publishDate).getTime())
    .slice(0, limit);
};

export const getPopularPosts = (limit: number = 10): BlogPost[] => {
  // Mock popular posts based on readTime (assuming higher readTime = more popular)
  return mockPosts
    .sort((a, b) => b.readTime - a.readTime)
    .slice(0, limit);
};